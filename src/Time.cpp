#include "../headers/Time.h"

Time& Time::getTime(){
    // Guarantees Destruction
    static Time instance;
    return instance;
}

bool Time::update(){
    simulationTime += simulationStep;
    return (simulationTime < maxSimulationTime);
}

Time::Time(){
    simulationTime = 0;
    simulationStep = 0.5e-4;
    maxSimulationTime = 1e-4;
}

double Time::getSimulationTime(){
    return simulationTime;
}

double Time::getTimeStep(){
    return simulationStep;
}

double Time::getMaxSimualationTime(){
    return maxSimulationTime;
}


